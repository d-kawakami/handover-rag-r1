# ─── 同義語 ──────────────────────────────────────────────────────────────
# 水処理施設ドメイン同義語グループ。検索語がいずれかに該当する場合
# グループ全体を候補にする。
SYNONYM_GROUPS: list[frozenset[str]] = [
    frozenset(["VVVF", "インバータ", "インバーター", "可変電圧可変周波数"]),
    frozenset(["PAC", "ポリ塩化アルミニウム", "凝集剤"]),
]


def expand_synonyms(terms: list[str]) -> list[str]:
    """トークンリスト中に既知の同義語グループの要素があれば、グループ全体を追加して返す。"""
    expanded = list(terms)
    for group in SYNONYM_GROUPS:
        if any(t in group for t in terms):
            for syn in group:
                if syn not in expanded:
                    expanded.append(syn)
    return expanded
