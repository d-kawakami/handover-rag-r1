_SUGGEST_PROMPT_TEMPLATE = """\
以下は水処理施設の引き継ぎノートの記録です。
この文書に含まれる専門用語・設備名・機器名・現場略語を抽出してください。

【抽出条件】
・一般的な国語辞典に載っていない専門用語
・設備名・機器名（例: UV計, 冷却水ポンプ, 最終沈殿池）
・現場略語（例: 最沈, CWP, ばっ気）
・アルファベット混じりの機器名（例: DO計, MLSS計）
・複合語で分割されると意味が変わる語

【出力形式】JSONのみ出力。説明文・Markdownコードブロック不要。
{{
  "candidates": [
    {{
      "surface": "文書中の表記そのまま",
      "normalized": "統一したい正規化表記（不明なら空文字）",
      "reason": "候補とした理由（短く）"
    }}
  ]
}}

【引き継ぎノート】
{sampled_text}
"""


def format_suggest_prompt(sampled_text: str) -> str:
    return _SUGGEST_PROMPT_TEMPLATE.format(sampled_text=sampled_text)
