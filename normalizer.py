"""
施設名・設備番号の表記揺れ正規化。
BM25トークナイザ・全文スキャン・インジェストの各パスで共通して使用する。
"""
import re
import unicodedata

# 漢数字→算用数字（ポンプ施設名・系列名に限定して使用）
_KANJI_DIGIT_MAP: dict[str, str] = {
    '一': '1', '二': '2', '三': '3', '四': '4', '五': '5',
    '六': '6', '七': '7', '八': '8', '九': '9',
}


def _series_sep(m: re.Match) -> str:
    """2桁隣接数字+系 の区切り文字を決定する。
    連続する数字ペア（7・8等）はナカグロ、飛び番号（1〜5等）は波ダッシュを使う。"""
    n1, n2 = int(m.group(1)), int(m.group(2))
    sep = '・' if n2 == n1 + 1 else '〜'
    return f'{n1}{sep}{n2}系列'


def normalize_notation(text: str) -> str:
    """施設名・設備番号の表記揺れを正規化する。

    正規化の順序:
      1. NFKC（全角英数字→半角）
      2. 漢数字施設名（第二ポンプ施設→第2ポンプ施設）
      3. ポンプ施設略称（2ポン・2P→第2ポンプ施設）
      4. 主P→主ポンプ
      5. 系列範囲（1-5系・1〜5系→1〜5系列）
      6. 多系列区切り付き（7・8系・7.8系→7・8系列）
      7. 多系列区切りなし（78系→7・8系列、15系→1〜5系列）
      8. 第N系→第N系列
      9. 単独N系→第N系列
      10. No.正規化（№N・NoN→No.N）
    """
    # 1. NFKC正規化（全角英数字・記号→半角）
    text = unicodedata.normalize('NFKC', text)

    # 2. 漢数字ポンプ施設名: 第二ポンプ施設→第2ポンプ施設
    for kanji, num in _KANJI_DIGIT_MAP.items():
        text = text.replace(f'第{kanji}ポンプ施設', f'第{num}ポンプ施設')

    # 3a. ポンプ施設略称: 2ポン→第2ポンプ施設
    text = re.sub(r'([1-9])ポン(?!プ)', r'第\1ポンプ施設', text)

    # 3b. ポンプ施設略称: 2P→第2ポンプ施設（英数字またはドットの後は変換しない）
    #     "No.5P" などナンバリング後の P を誤変換しないよう . も除外する
    text = re.sub(r'(?<![A-Za-z0-9.])([1-9])P(?![A-Za-z0-9])', r'第\1ポンプ施設', text)

    # 4. 主P→主ポンプ
    text = re.sub(r'主P(?![A-Za-z0-9])', '主ポンプ', text)

    # 5. 系列範囲（ハイフン・ダッシュ系）: 1-5系[列]・1−5系[列]→1〜5系列
    #    ※漢字が後続する場合（系統・系図等）は変換しない
    text = re.sub(
        r'([0-9])[~\-−–—]([0-9])系(?:列)?(?![一-鿿])',
        r'\1〜\2系列',
        text,
    )

    # 6. 系列範囲（〜）: 1〜5系→1〜5系列
    text = re.sub(r'([0-9])〜([0-9])系(?!列|[一-鿿])', r'\1〜\2系列', text)

    # 7. 多系列区切り付き: 7・8系 / 7.8系→7・8系列
    text = re.sub(r'([0-9])[・.]([0-9])系(?![一-鿿])', r'\1・\2系列', text)

    # 8. 多系列区切りなし: 78系→7・8系列 / 15系→1〜5系列
    #    連続数字ペア(n, n+1)はナカグロ、飛び番号は波ダッシュ
    text = re.sub(r'(?<![0-9])([1-9])([1-9])系(?![一-鿿])', _series_sep, text)

    # 9a. 第N系→第N系列（漢字後続なし）
    text = re.sub(r'第([0-9]+)系(?![一-鿿])', r'第\1系列', text)

    # 9b. 単独N系→第N系列（前が数字・中黒・波ダッシュでない1桁数字）
    text = re.sub(r'(?<![0-9・〜])([1-9])系(?![一-鿿])', r'第\1系列', text)

    # 10. No.正規化: №N / NO.N / NoN→No.N（前が英字でない場合のみ）
    #     NFKC後 № は "No" に変換済みのため、そちらも補足
    text = re.sub(r'(?<![A-Za-z])[Nn][Oo]\.?([0-9]+)', r'No.\1', text)

    return text
